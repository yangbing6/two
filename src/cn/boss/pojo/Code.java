package cn.boss.pojo;

public class Code {
	private Integer co_id;
	private String co_number;
	private String co_pwd;
	public Integer getCo_id() {
		return co_id;
	}
	public void setCo_id(Integer co_id) {
		this.co_id = co_id;
	}
	public String getCo_number() {
		return co_number;
	}
	public void setCo_number(String co_number) {
		this.co_number = co_number;
	}
	public String getCo_pwd() {
		return co_pwd;
	}
	public void setCo_pwd(String co_pwd) {
		this.co_pwd = co_pwd;
	}
}
